/**
 * Fetch brief metrics for purchase dashboard analytics.
 */
export async function getPurchaseDashboardMetrics() {
  try {
    const today = new Date();
    today.setHours(0, 0, 0, 0);

    const completedStatus: PurchaseStatus = "COMPLETED";

    const [allCompleted, todayCompleted, pendingPayments] = await Promise.all([
      // 1. Total Purchases
      prisma.purchase.aggregate({
        where: { status: completedStatus },
        _sum: { grandTotal: true },
      }),
      // 2. Today's Purchases
      prisma.purchase.aggregate({
        where: {
          status: completedStatus,
          purchaseDate: {
            gte: today,
          },
        },
        _sum: { grandTotal: true },
      }),
      // 3. Pending Payments
      prisma.purchase.aggregate({
        where: {
          status: completedStatus,
          paymentStatus: {
            in: ["UNPAID", "PARTIALLY_PAID"] as PurchasePaymentStatus[],
          },
        },
        _sum: { grandTotal: true },
      }),
    ]);

    return {
      success: true,
      data: {
        totalPurchases: Number(allCompleted._sum.grandTotal || 0),
        todayPurchases: Number(todayCompleted._sum.grandTotal || 0),
        pendingPayments: Number(pendingPayments._sum.grandTotal || 0),
      },
    };
  } catch (error) {
    console.error("getPurchaseDashboardMetrics error:", error);
    return { success: false, error: "Failed to load dashboard metrics." };
  }
}

